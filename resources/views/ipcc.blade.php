    <h1>IES Pasqual Calbó i Caldés (IPCC)</h1>
    <p>Agua, vida y bienestar es un proyecto interdisciplinar en qué a partir de unos intereses comunes cada centro los
        desarrolla en función de su entorno, haciendo hincapié en las necesidades locales. El alumnado destinatario varía
        en función del centro y sus características. Utilizamos metodologías innovadoras, el trabajo por proyectos ABP y
        globalizados, donde el profesorado de distintas áreas y materias colaboran entorno a un eje común, el agua,
        desarrollando las competencias clave del currículum a partir de una pregunta, desafío o situación de aprendizaje
        motivadora. A su vez, también se aplica la metodología STEAM como por ejemplo participación en los retos
        AquaSteam de la Universidad Politécnica de Cataluña (UPC) o en la elaboración del proyecto artístico transversal.
        Con el uso de estas metodologías pretendemos mejorar, por una parte, la colaboración entre departamentos,
        romper barreras y prejuicios, construir conocimiento a través de la sabiduría colectiva, para dar un salto de calidad
        en la educación; Y por el otro, potenciar el interés y motivación del alumnado, básico para el aprendizaje, asimismo
        fomentar la inclusión educativa.
        Al trabajar por proyectos, todos los estudiantes desempeñan un rol, se sienten partícipes del grupo y pueden
        aportar sus distintas habilidades. En este sentido el INS Manresa SIS, lleva desde sus inicios, curso 2017-2018,
        utilizando esta metodología y puede ser fuente de inspiración para los otros centros de la agrupación.
        Nos basamos en los principios del aprendizaje inductivo, del “learning by doing” (Dewey) y del “role playing”. El
        estudiantado enseñará, a partir de talleres o presentaciones orales impartidos por éste, y con ayuda y supervisión
        del profesorado, lo que va aprendiendo en el proyecto de cada centro. Los talleres impartidos por el alumnado
        sobre los diversos temas que articulan el proyecto (ODS, gestión del agua, etc) serán puestos a disposición, primero
        de los centros implicados y luego del público en general, a través de la página web del proyecto que se convertirá
        en una red de formación educativa. De este modo pretendemos que se implique más en su aprendizaje, ya que
        tendrá que actuar como formador, a la vez que damos un contexto significativo a las actividades. Logramos también
        así la retroalimentación entre el estudiantado de los distintos centros durante los dos años de duración del
        proyecto.
        Para potenciar la equidad y la inclusión educativa, y reducir la segregación escolar, es necesario implementar
        acciones y estrategias dirigidas a fomentar la presencia, la participación y los logros de todo el alumnado,
        especialmente aquellos que son especialmente vulnerables.</p>
